#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*res;
	int		i;
	int		j;

	i = size;
	j = 0;
	res = (char*)malloc(size + 1);
	if (res != NULL)
	{
		while (j <= i)
			res[j++] = '\0';
	}
	return (res);
}
