#include "libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	int		f;
	long	n;

	i = 0;
	f = 1;
	n = 0;
	while (str[i] == '\t' || str[i] == '\r' || str[i] == '\n'
			|| str[i] == '\f' || str[i] == ' ' || str[i] == '\v')
		i++;
	f = (str[i] == '-' ? -1 : 1);
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + (str[i] - '0');
		i++;
		if (i == 20 && f == -1)
			return (0);
		else if (i == 19 && f != -1)
			return (-1);
	}
	return ((int)n * f);
}
